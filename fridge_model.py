def _fridge_normalize_input(text: str) -> str:
    """Подготовить пользовательский список: HTML/маркированный текст -> строки продуктов."""
    t = html.unescape(str(text or ""))
    t = re.sub(r"(?i)<\s*br\s*/?\s*>", "\n", t)
    t = re.sub(r"(?i)<\s*/\s*li\s*>", "\n", t)
    t = re.sub(r"(?i)<\s*li[^>]*>", "\n", t)
    t = re.sub(r"(?i)<\s*/?\s*(ul|ol)[^>]*>", "\n", t)
    t = re.sub(r"<[^>]+>", "", t)
    return t


def _fridge_detect_cat(name: str) -> str | None:
    """Определить одну из шести категорий или вернуть None при низкой уверенности."""
    n = str(name or "").lower()
    if any(marker in n for marker in ("заморож", "замороз", "морожен", "frozen", "diepvries")):
        return "заморозка"
    if n in _FRIDGE_CAT_EXACT:
        return _FRIDGE_CAT_EXACT[n]
    if any(phrase in n for phrase in ("томатная паст", "паста том", "том ям", "том-ям", "том кха", "tom yam", "tom kha")):
        return "специи и соусы"
    if n in ("ui", "uien", "sla", "kropsla"):
        return "овощи и фрукты"
    if n == "ham":
        return "мясо и рыба"
    if n in ("water", "mineraalwater"):
        return "молочное и напитки"
    if n == "watermeloen":
        return "овощи и фрукты"
    if n in ("paprika", "rode paprika", "gele paprika", "groene paprika"):
        return "овощи и фрукты"
    if "перец" in n:
        if any(k in n for k in ("чёрн", "черн", "молот", "душист", "чили", "cayenne", "кайен")):
            return "специи и соусы"
        return "овощи и фрукты"
    if "koolsla" in n or "coleslaw" in n or "cole slaw" in n or "коул слоу" in n or "кол слоу" in n:
        return "овощи и фрукты"
    for cat, keywords in _FRIDGE_KEYWORDS.items():
        if any(k in n for k in keywords):
            return cat
    return None


def _fridge_cat(name: str) -> str:
    """Совместимый детерминированный fallback без категории «Прочее».

    Интерактивное добавление использует _fridge_detect_cat() и при None просит
    пользователя выбрать категорию. Fallback нужен для старых записей и
    неинтерактивных сценариев миграции.
    """
    return _fridge_detect_cat(name) or "бакалея"


